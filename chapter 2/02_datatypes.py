# Integer
age = 20
print("Age:", age, "Type:", type(age))   #type()  data ka type batata hain

# Float
height = 5.9
print("Height:", height, "Type:", type(height))

# String
name = "Rushuu"
print("Name:", name, "Type:", type(name))

# Boolean
is_student = True
print("Student:", is_student, "Type:", type(is_student))

# List
numbers = [1, 2, 3, 4]
print("Numbers:", numbers, "Type:", type(numbers))

# Tuple
coordinates = (10, 20)
print("Coordinates:", coordinates, "Type:", type(coordinates))

# Dictionary
student = {"name": "Rushuu", "age": 20}
print("Student Info:", student, "Type:", type(student))

# Set
unique_numbers = {1, 2, 3, 4}
print("Unique Numbers:", unique_numbers, "Type:", type(unique_numbers))