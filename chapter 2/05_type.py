# Basic Type Casting
a = 10
b = 12.75
c = "50"
d = "45.6"

print("int to float:", float(a))
print("float to int:", int(b))
print("str to int:", int(c))
print("str to float:", float(d))
print("int to str:", str(a))


# Data Structure Type Casting
list_data = [1, 2, 3]
tuple_data = (4, 5, 6)
set_data = {7, 8, 9}

print("list to tuple:", tuple(list_data))
print("tuple to list:", list(tuple_data))
print("set to list:", list(set_data))
print("set to tuple:", tuple(set_data))